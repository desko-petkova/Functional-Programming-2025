-- 1. Функция, която обръща списък
reverseList :: [a] -> [a]       -- Декларираме тип: reverseList приема списък [a] и връща списък [a]
reverseList [] = []             -- Базов случай: ако списъкът е празен, обръщането му е празен списък
reverseList (x:xs) = reverseList xs ++ [x] -- Рекурсивен случай: обръщаме опашката xs, после добавяме елемента x накрая

-- 2.Напиши функция, която добавя елемент към края на списък.
addToEnd :: a -> [a] -> [a]             -- Тип: addToEnd приема елемент x и списък [a], връща нов списък с x накрая
addToEnd x [] = [x]                     -- Ако списъкът е празен, просто връщаме списък, съдържащ само x
addToEnd x (y:ys) = y : addToEnd x ys   -- Иначе взимаме първия елемент y и го добавяме отпред,
                                        -- а рекурсивно добавяме x към края на опашката ys

-- 3. Функция, която връща последния елемент от списък
lastElement :: [a] -> a             -- Тип: lastElement приема списък [a] и връща един елемент от тип a
lastElement [] = error "Empty list!" -- Ако списъкът е празен, хвърляме грешка, защото няма последен елемент
lastElement (x:[]) = x              -- Ако списъкът има точно един елемент, това е последният елемент
lastElement (_:xs) = lastElement xs -- Иначе игнорираме първия елемент и търсим последния в опашката xs

--4. Сумиране на елементите на списък
sumList :: [Int] -> Int             -- Тип: sumList приема списък от Int и връща Int (сумата)
sumList [] = 0                        -- Базов случай: сумата на празен списък е 0
sumList (x:xs) = x + sumList xs     -- Рекурсивно: първият елемент x + сумата на останалите xs

--5. Брой на елементите в списък
count :: [a] -> Int                 -- Тип: count приема списък и връща Int — броя на елементите
count [] = 0                        -- Базов случай: празният списък има 0 елемента
count (_:xs) = 1 + count xs         -- Рекурсивно: игнорираме първия елемент и добавяме 1 към броя на остатъка
